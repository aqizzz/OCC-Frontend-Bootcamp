// assignment expressions
let x = 10;
let y = 5;

// assignment expressions - addition
x += y;
console.log("x = " + x + " y = " + y);
x += 1;
console.log("x = " + x + " y = " + y);

// assignment expressions - subtraction
let a = 10;
let b = 5;
a -= b;
console.log("a = " + a + " b = " + b);

// assignment expression - multiplication
let r = 10;
let s = 5;
r *= s;
console.log("r = " + r + " s = " + s);

// assignment expression - division
let d = 10;
let e = 5;
d /= e;
console.log("d = " + d + " e = " + e);