// for loop
for (let i = 1; i <= 5; i++) {
  console.log("Count:", i);
}


//  while loop
let count = 1;
while (count <= 5) {
  console.log('while Count:', count);
  count++;
}

// Loops: do-while loop
let i = 1;
do {
  console.log('Do-While Count:', i);
  i++;
} while (i <= 5);